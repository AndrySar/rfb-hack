package ru.rfb.hack.domain.dto;

public class CoordinatesDTO {

    private Double lat;
    private Double lon;

    public CoordinatesDTO(Double lat, Double lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}
